package chapter10.format;

import java.text.ChoiceFormat;

public class ChoiceFormatPractice1 {

	public static void main(String[] args) {
		double[] limits = {60, 70, 80, 90};// 낮은 값부터 큰 값의 순서로 적어야 한다.
		// limits, grades 간의 순서와 개수를 맞추어야 한다.
		String[] grades = {"D", "C", "B", "A"};
		
		int[] scores = { 100, 95, 88, 70, 52, 60, 70 };
		
		ChoiceFormat form = new ChoiceFormat(limits, grades);
		
		for(int i=0; i<scores.length; i++){
			System.out.println(scores[i]+ " : " + form.format(scores[i]));
		}
		
		System.out.println();
		
		// 패턴은 구분자로 # 과  < 두가지를 제공하는데 'limit#value'의 형태로 사용한다.
		// #은 경계값을 범위에 포함시키지만 < 는 포함시키지 않는다. 아래의 결과가 90은 A지만, 80은 B가 아닌, C인것에 주목하자.
		String pattern = "60 # D | 70 # C | 80 < B | 90 # A";
		int[] scores2 = { 91, 90, 80, 88, 70, 52, 60 };
		
		ChoiceFormat form2 = new ChoiceFormat(pattern);
		
		for(int i=0; i< scores2.length; i++){
			System.out.println(scores2[i]+ " : "+ form2.format(scores2[i]));
		}

	}

}
